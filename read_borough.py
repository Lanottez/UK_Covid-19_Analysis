

def rb(length):
	if length == 12:
		return 'Inner London'
	elif length == 20:
		return 'Outer London'
	elif length == 6:
		return 'Central London'
	elif length == 7:
		return 'West London'
	elif length == 3:
		return 'North London'
	elif length == 6:
		return 'South London'
	elif length == 10:
		return 'East London'
	else:
		print('error')
